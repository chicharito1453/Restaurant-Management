    void search() {
        DefaultTableModel model = (DefaultTableModel) tblDSM.getModel();
        model.setRowCount(0);
        String ndTim = txtTim.getText().trim();
        List<DANHSACHMON> list = dsmdao.selectAll();
        String[] str = null;
        boolean tim = false;
        int count = 0;
        if (txtTim.getForeground() == Color.black) {
            str = ndTim.split(" ");
            tim = true;
        }
        for (int i = 0; i < list.size(); i++) {
            if (tim && ndTim.equalsIgnoreCase(list.get(i).getTenMon())) {
                setForm(list.get(i));
                model.addRow(new Object[]{
                    list.get(i).getIdMon(), list.get(i).getTenMon(), list.get(i).isLoai() ? "Thức ăn" : "Nước uống", list.get(i).getDonGia()
                });
                row = i;
                tblDSM.setRowSelectionInterval(row, row);
                count ++;
                break;
            } else if (tim) {
                for (int j = 0; j < str.length; j++) {
                    if (list.get(i).getTenMon().toLowerCase().contains(str[j].toLowerCase())) {
                        count --;
                        model.addRow(new Object[]{
                            list.get(i).getIdMon(), list.get(i).getTenMon(), list.get(i).isLoai() ? "Thức ăn" : "Nước uống", list.get(i).getDonGia()
                        });
                        row = tblDSM.getRowCount()-1;
                        setForm(list.get(i));
                    }
                }
            }
        }
        if (count == 0) {
            filltable();
            MsgBox.alert(tab, "Không tìm thấy món!");
        } else if(count == 1) {
            MsgBox.alert(tab, "Đã tìm thấy món!");
        }
        else{
            MsgBox.alert(tab, "Có thể bạn tìm các món này!");
        }
    }
    
