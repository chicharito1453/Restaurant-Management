    void insert() {
        if (check()) {
            DANHSACHMON dsm = getForm();
            dsmdao.insert(dsm);
            filltable();
            clear();
            MsgBox.alert(tab, "Thêm món thành công!");
        }
    }

    void update() {
        if (lblMaMon.getText().trim().isEmpty()) {
            MsgBox.alert(tab, "Chọn món cần cập nhật!");
        } else {
            if (check()) {
                DANHSACHMON dsm = getForm();
                dsm.setIdMon(Integer.parseInt(lblMaMon.getText()));
                dsmdao.update(dsm);
                filltable();
                MsgBox.alert(tab, "Cập nhật thành công!");
            }
        }
    }

    void delete() {
        if (lblMaMon.getText().isEmpty()) {
            MsgBox.alert(tab, "Chọn món cần xóa!");
        } else {
            if (MsgBox.confirm(tab, "Bạn có chắc muốn xóa món này?")) {
                hdctdao.deleteTheoMon(Integer.parseInt(lblMaMon.getText()));
                dsmdao.delete(Integer.parseInt(lblMaMon.getText()));
                filltable();
                clear();
                MsgBox.alert(tab, "Đã xóa thành công!");
            }
        }
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblDSM.getModel();
        model.setRowCount(0);
        List<DANHSACHMON> list = dsmdao.selectAll();
        if (list.size() > 0) {
            for (DANHSACHMON dsm : list) {
                Object[] row = {dsm.getIdMon(), dsm.getTenMon(), dsm.isLoai() ? "Thức ăn" : "Nước uống", dsm.getDonGia()
                };
                model.addRow(row);
            }
        }
    }

    void clear() {
        lblMaMon.setText("");
        txtTenMon.setText("");
        txtGia.setText("");
        rboThucAn.setSelected(false);
        rboNuocUong.setSelected(false);
        lblHinhAnh.setToolTipText("noimage2.jpg");
        lblHinhAnh.setIcon(XImage.read("noimage2.jpg"));
        txtTenMon.requestFocus();
    }

    boolean check() {
        if (txtTenMon.getText().trim().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập tên món!");
            return false;
        } else if (!rboThucAn.isSelected() && !rboNuocUong.isSelected()) {
            MsgBox.alert(this, "Chưa chọn loại món!");
            return false;
        } else if (txtGia.getText().trim().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập giá!");
            return false;
        } else if (!txtGia.getText().trim().matches("\\d+")) {
            MsgBox.alert(this, "Giá phải là số!");
            return false;
        } else {
            return true;
        }
    }

    DANHSACHMON getForm() {
        DANHSACHMON dsm = new DANHSACHMON();
        dsm.setTenMon(txtTenMon.getText().trim());
        if (rboThucAn.isSelected()) {
            dsm.setLoai(true);
        }
        if (rboNuocUong.isSelected()) {
            dsm.setLoai(false);
        }
        dsm.setDonGia(Double.parseDouble(txtGia.getText().trim()));
        dsm.setHinhanh(lblHinhAnh.getToolTipText());
        return dsm;
    }

    void setForm(DANHSACHMON dsm) {
        lblMaMon.setText(String.valueOf(dsm.getIdMon()));
        txtTenMon.setText(dsm.getTenMon());
        if (dsm.isLoai()) {
            rboThucAn.setSelected(true);
            rboNuocUong.setSelected(false);
        } else {
            rboThucAn.setSelected(false);
            rboNuocUong.setSelected(true);
        }
        txtGia.setText(String.valueOf((int) dsm.getDonGia()));
        try {
            Image img = this.read(dsm.getHinhanh());
            Image resizedImage = img.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), Image.SCALE_SMOOTH);
            lblHinhAnh.setIcon(new ImageIcon(resizedImage));
            lblHinhAnh.setToolTipText(dsm.getHinhanh());
        } catch (Exception e) {
        }
    }

